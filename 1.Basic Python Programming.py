print("Hello, World!")

# Practicing basic data types, variables, and operators

# Variables and data types
integer_var = 10  # Integer
float_var = 20.5  # Float
string_var = "Hello, Python!"  # String
boolean_var = True  # Boolean

# Arithmetic operators
sum_result = integer_var + float_var
difference = float_var - integer_var
product = integer_var * 2
division = float_var / 2

# Comparison operators
is_equal = integer_var == 10
is_greater = float_var > integer_var

# Logical operators
and_result = boolean_var and (integer_var > 5)
or_result = boolean_var or (integer_var < 5)

# Output results
print("Integer:", integer_var)
print("Float:", float_var)
print("String:", string_var)
print("Boolean:", boolean_var)
print("Sum:", sum_result)
print("Difference:", difference)
print("Product:", product)
print("Division:", division)
print("Is Equal:", is_equal)
print("Is Greater:", is_greater)
print("AND Result:", and_result)
print("OR Result:", or_result)